import multiprocessing as mp

def foo(q):
	q.put('hello')

if __name__ == '__main__':
	mp.set_start_method('spawn')
	q = mp.Queue()
	for s in range(mp.cpu_count()):
		p = mp.Process(target=foo, args=(q,))
		p.start()
		print(q.get())
		p.join()

